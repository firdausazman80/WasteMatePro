package com.example.wastematepro.Models;

public class TasksModel {

    private String name;

    private int image;

    public TasksModel(String name) {
        this.name = name;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }
}
